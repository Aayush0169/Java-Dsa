public class operators  {
    public static void main(String arg[]){
         System.out.println("& operator: "+(5&6));
         System.out.println("| operator: "+(5|6));
         System.out.println("^ operator: "+(5^6));
         System.out.println("~ (single bit)operator: "+(~5)+","+(~6));
         System.out.println("<< operator: "+(5<<6));
         System.out.println(">> operator: "+(5>>1));
    }
}